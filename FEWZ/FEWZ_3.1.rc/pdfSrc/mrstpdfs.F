      subroutine jeppe2(x,y,nx,my,xx,yy,cc,z)
      implicit real*8(a-h,o-z)
      dimension xx(nx),yy(my),cc(nx,my,4,4)      

      n=locx(xx,nx,x)
      m=locx(yy,my,y)

      t=(x-xx(n))/(xx(n+1)-xx(n))
      u=(y-yy(m))/(yy(m+1)-yy(m))

      z=0.
      do 1 l=4,1,-1
      z=t*z+((cc(n,m,l,4)*u+cc(n,m,l,3))*u
     .       +cc(n,m,l,2))*u+cc(n,m,l,1)
    1 continue
      return
      end

C----------------------------------------------------------------------
C--   Fortran interpolation code for MRST 2006 NNLO PDFs, building
C--   on existing MRST Fortran code and J.Andersen's C++ code.
C--   Three user interfaces:
C--    call GetAllPDFs(prefix,i,x,q,
C--                    upv,dnv,usea,dsea,str,sbar,chm,bot,glu)
C--    call GetAllPDFsAlt(prefix,i,x,q,xpdf)
C--    xf = GetOnePDF(prefix,i,x,q,f)
C--   See enclosed example.f for usage.
C--   Comments to watt(at)hep.ucl.ac.uk
C----------------------------------------------------------------------

C----------------------------------------------------------------------

C--   Traditional MRST-like interface: return all flavours.
C--   (Note the additional "sbar" compared to previous MRST releases.)
      subroutine GetAllPDFs6(prefix,i,x,q,
     &     upv,dnv,usea,dsea,str,sbar,chm,bot,glu)
      implicit none
      integer i
      double precision x,q,upv,dnv,usea,dsea,str,sbar,chm,bot,glu,
     &     GetOnePDF6
      character*150 prefix

      upv  = GetOnePDF6(prefix,i,x,q,1)
      dnv  = GetOnePDF6(prefix,i,x,q,2)
      usea = GetOnePDF6(prefix,i,x,q,4)
      dsea = GetOnePDF6(prefix,i,x,q,8)
      str  = GetOnePDF6(prefix,i,x,q,6)
      sbar = GetOnePDF6(prefix,i,x,q,9)
      chm  = GetOnePDF6(prefix,i,x,q,5)
      bot  = GetOnePDF6(prefix,i,x,q,7)
      glu  = GetOnePDF6(prefix,i,x,q,3)

      return
      end

C----------------------------------------------------------------------

C--   Alternative LHAPDF-like interface: return PDFs in an array.
      subroutine GetAllPDFsAlt6(prefix,i,x,q,xpdf)
      implicit none
      integer i
      double precision x,q,xpdf(-6:6),
     &     upv,dnv,usea,dsea,str,sbar,chm,bot,glu
      character*150 prefix

      call GetAllPDFs6(prefix,i,x,q,
     &     upv,dnv,usea,dsea,str,sbar,chm,bot,glu)
      xpdf(-6) = 0.d0           ! tbar
      xpdf(-5) = bot            ! bbar
      xpdf(-4) = chm            ! cbar
      xpdf(-3) = sbar           ! sbar
      xpdf(-2) = usea           ! ubar
      xpdf(-1) = dsea           ! dbar
      xpdf(0) = glu             ! g
      xpdf(1) = dnv+dsea        ! d
      xpdf(2) = upv+usea        ! u
      xpdf(3) = str             ! s
      xpdf(4) = chm             ! c
      xpdf(5) = bot             ! b
      xpdf(6) = 0.d0            ! t
      
      return
      end

