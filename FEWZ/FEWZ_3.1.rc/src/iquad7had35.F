      tncoff3 = 1/(-2+l1)/l1/t5/t10/t12*t15/4096
      t3 = Diquad722(z,0.D0,0.D0,0.D0,l4)
      t6 = s34r2(z,1.D0,1.D0,0.D0,l4)
      t8 = s23r2(z,1.D0,1.D0,0.D0,l4)
